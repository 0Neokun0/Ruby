# Ruby program to illustrate each iterator


# using each iterator
# here collection is range
# variable name is i
(0..9).each do |i|
	
	# statement to be executed
	puts i
	
end

a = ['G', 'e', 'e', 'k', 's']

puts "\n"

# using each iterator
# here collection is an array
a.each do|arr|

	# statement to be executed
	puts arr
	
end
